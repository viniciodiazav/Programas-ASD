LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;
USE IEEE.NUMERIC_STD.ALL;
USE IEEE.STD_LOGIC_UNSIGNED.ALL;

ENTITY p8 IS 
PORT (
	CLK, X : IN STD_LOGIC;
	D7 : OUT STD_LOGIC_VECTOR(13 DOWNTO 0)
);
END p8;

ARCHITECTURE ecu OF p8 IS

	SIGNAL COUNT : STD_LOGIC_VECTOR(27 DOWNTO 0);
	
	TYPE TYPES IS (
		M0, M5, M10, M15, M20, M25, M30,
		P0, P2, P3, P5, P7, P11, P13, P17, P19, P23, P29, P31
	);
	
	SIGNAL STATE, NEXT_STATE : TYPES;
	
BEGIN
	
	PROCESS(CLK)
	BEGIN
	
		IF rising_edge(CLK) THEN
			IF COUNT = "0001011111010111100001000000" THEN
				COUNT <= (OTHERS => '0');
				STATE <= NEXT_STATE;
			ELSE
				COUNT <= COUNT + '1';
			END IF;
		END IF;
		
	END PROCESS;
				
	PROCESS(STATE, X)
	BEGIN
	
		CASE STATE IS
		
			WHEN M0 =>
				IF X = '0' THEN NEXT_STATE <= M30;
				ELSE NEXT_STATE <= P0;
				END IF;
				
			WHEN M30 =>
				IF X = '0' THEN NEXT_STATE <= M25;
				ELSE NEXT_STATE <= P0;
				END IF;
				
			WHEN M25 =>
				IF X = '0' THEN NEXT_STATE <= M20;
				ELSE NEXT_STATE <= P0;
				END IF;
				
			WHEN M20 =>
				IF X = '0' THEN NEXT_STATE <= M15;
				ELSE NEXT_STATE <= P0;
				END IF;
				
			WHEN M15 =>
				IF X = '0' THEN NEXT_STATE <= M10;
				ELSE NEXT_STATE <= P0;
				END IF;
				
			WHEN M10 =>
				IF X = '0' THEN NEXT_STATE <= M5;
				ELSE NEXT_STATE <= P0;
				END IF;
				
			WHEN M5 =>
				IF X = '0' THEN NEXT_STATE <= M0;
				ELSE NEXT_STATE <= P0;
				END IF;
				
			WHEN P0 =>
				IF X = '1' THEN NEXT_STATE <= P31;
				ELSE NEXT_STATE <= M0;
				END IF;
				
			WHEN P31 =>
				IF X = '1' THEN NEXT_STATE <= P29;
				ELSE NEXT_STATE <= M0;
				END IF;
	
			WHEN P29 =>
				IF X = '1' THEN NEXT_STATE <= P23;
				ELSE NEXT_STATE <= M0;
				END IF;
				
			WHEN P23 =>
				IF X = '1' THEN NEXT_STATE <= P19;
				ELSE NEXT_STATE <= M10;
				END IF;
				
			WHEN P19 =>
				IF X = '1' THEN NEXT_STATE <= P17;
				ELSE NEXT_STATE <= M0;
				END IF;
	
			WHEN P17 =>
				IF X = '1' THEN NEXT_STATE <= P13;
				ELSE NEXT_STATE <= M0;
				END IF;
				
			WHEN P13 =>
				IF X = '1' THEN NEXT_STATE <= P11;
				ELSE NEXT_STATE <= M0;
				END IF;
	
			WHEN P11 =>
				IF X = '1' THEN NEXT_STATE <= P7;
				ELSE NEXT_STATE <= M0;
				END IF;
	
			WHEN P7 =>
				IF X = '1' THEN NEXT_STATE <= P5;
				ELSE NEXT_STATE <= M0;
				END IF;
				
			WHEN P5 =>
				IF X = '1' THEN NEXT_STATE <= P3;
				ELSE NEXT_STATE <= M0;
				END IF;
	
			WHEN P3 =>
				IF X = '1' THEN NEXT_STATE <= P2;
				ELSE NEXT_STATE <= M0;
				END IF;
	
			WHEN P2 =>
				IF X = '1' THEN NEXT_STATE <= P31;
				ELSE NEXT_STATE <= M0;
				END IF;
		
			END CASE;
	
	END PROCESS;
		
	PROCESS(STATE)
	BEGIN
		
		CASE STATE IS       --gfedcbagfedcba
			WHEN M0 	=> D7 <= "11111111000000";
			WHEN M5 	=> D7 <= "11111110010010";
			WHEN M10 => D7 <= "11110011000000";
			WHEN M15 => D7 <= "11110010010010";
			WHEN M20 => D7 <= "01001001000000";
			WHEN M25 => D7 <= "01001000010010";
			WHEN M30 => D7 <= "01100001000000";
			WHEN P0 	=> D7 <= "11111111000000";
			WHEN P2 	=> D7 <= "11111110100100";
			WHEN P3 	=> D7 <= "11111110110000";
			WHEN P5 	=> D7 <= "11111110010010";
			WHEN P7  => D7 <= "11111111111000";
			WHEN P11 => D7 <= "11110011111001";
			WHEN P13 => D7 <= "11110010110000";
			WHEN P17 => D7 <= "11110011111000";
			WHEN P19 => D7 <= "11110010010000";
			WHEN P23 => D7 <= "01001000110000";
			WHEN P29 => D7 <= "01001000010000";
			WHEN P31 => D7 <= "01100001111001";
		END CASE;
			
	END PROCESS;
		
END ecu;
