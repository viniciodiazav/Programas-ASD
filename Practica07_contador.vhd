LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;
USE IEEE.NUMERIC_STD.ALL;
USE IEEE.STD_LOGIC_UNSIGNED.ALL;

ENTITY p7 IS
PORT (
	CLKI : IN STD_LOGIC;
	D7 : OUT STD_LOGIC_VECTOR(6 DOWNTO 0)
);
END p7;

ARCHITECTURE ecu OF p7 IS

	SIGNAL COUNT : STD_LOGIC_VECTOR(27 DOWNTO 0);
	
	SIGNAL CLKINT : STD_LOGIC;
	
	SIGNAL D : STD_LOGIC_VECTOR(2 DOWNTO 0);
	
	SIGNAL Q2, Q1, Q0 : STD_LOGIC;

BEGIN

	D(2) <= (Q2 AND (Q1 XOR Q0)) OR (NOT (Q2 OR Q1 OR Q0));
	D(1) <= (Q1 NOR Q0) OR ((NOT Q2) AND Q1 AND Q0);
	D(0) <= (NOT Q0) AND ((Q1 XOR Q2) OR (Q2 AND Q1));


	PROCESS(CLKI)
	BEGIN
		
		IF rising_edge(CLKI) THEN
			IF COUNT = "0001011111010111100001000000" THEN
				COUNT <= (OTHERS => '0');
				CLKINT <= NOT CLKINT;
				Q2 <= D(2);
				Q1 <= D(1);
				Q0 <= D(0);
			ELSE
				COUNT <= COUNT + '1';
			END IF;
		END IF;
	END PROCESS;

	WITH D SELECT
		D7 <= "1000000" WHEN "000",
		      "1111001" WHEN "001",
		      "0100100" WHEN "010",
		      "0110000" WHEN "011",
		      "0011001" WHEN "100",
		      "0010010" WHEN "101",
		      "0000010" WHEN "110",
		      "1111111" WHEN OTHERS;
	

END ecu;
