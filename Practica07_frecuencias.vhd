LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;
USE IEEE.NUMERIC_STD.ALL;
USE IEEE.STD_LOGIC_UNSIGNED.ALL;

ENTITY p7 IS
PORT (
	CLKI : IN STD_LOGIC;
	F1, F2, F25 : OUT STD_LOGIC
);
END p7;

ARCHITECTURE ecu OF p7 IS

	SIGNAL CLKINT1, CLKINT2, CLKINT25 : STD_LOGIC;
	SIGNAL COUNT1, COUNT2, COUNT25 : STD_LOGIC_VECTOR(27 DOWNTO 0);

BEGIN

	PROCESS(CLKI)
	BEGIN
		IF rising_edge(CLKI) THEN
			IF COUNT1 = "0001011111010111100001000000" THEN
				COUNT1 <= (OTHERS => '0');
				CLKINT1 <= NOT CLKINT1;
				F1 <= CLKINT1;
			ELSE
				COUNT1 <= COUNT1 + '1';
			END IF;
			
			IF COUNT2 = "0000101111101011110000100000" THEN
				COUNT2 <= (OTHERS => '0');
				CLKINT2 <= NOT CLKINT2;
				F2 <= CLKINT2;
			ELSE
				COUNT2 <= COUNT2 + '1';
			END IF;
			
			IF COUNT25 = "0101111101011110000100000000" THEN
				COUNT25 <= (OTHERS => '0');
				CLKINT25 <= NOT CLKINT25;
				F25 <= CLKINT25;
			ELSE
				COUNT25 <= COUNT25 + '1';
			END IF;
		
	
		END IF;
	END PROCESS;



END ecu;
