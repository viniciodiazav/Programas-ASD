LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;
USE IEEE.STD_LOGIC_UNSIGNED.ALL;
USE IEEE.NUMERIC_STD.ALL;

ENTITY p6 IS
PORT (
	N1, N2: IN STD_LOGIC_VECTOR(2 DOWNTO 0);
	D7: OUT STD_LOGIC_VECTOR(8 DOWNTO 0)
);
END p6;

ARCHITECTURE ecu OF p6 IS

	SIGNAL SUM : STD_LOGIC_VECTOR(3 DOWNTO 0);

BEGIN

	SUM <= ('0' & N1) + ('0' & N2);
	
	WITH SUM SELECT
		D7 <= "111000000" WHEN "0000",
		      "111111001" WHEN "0001",
		      "110100100" WHEN "0010",
		      "110110000" WHEN "0011",
		      "110011001" WHEN "0100",
		      "110010010" WHEN "0101",
		      "110000010" WHEN "0110",
		      "111111000" WHEN "0111",
		      "110000000" WHEN "1000",
		      "110010000" WHEN "1001",
		      "001000000" WHEN "1010",
		      "001111001" WHEN "1011",
		      "000100100" WHEN "1100",
		      "000110000" WHEN "1101",
				"000011001" WHEN "1110",
		      "111111111" WHEN OTHERS;
				 
END ecu;
