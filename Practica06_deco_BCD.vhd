LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;
USE IEEE.STD_LOGIC_UNSIGNED.ALL;
USE IEEE.NUMERIC_STD.ALL;

ENTITY p6 IS
PORT (
	X : IN STD_LOGIC_VECTOR(3 DOWNTO 0);
	D7: OUT STD_LOGIC_VECTOR(6 DOWNTO 0)
);
END p6;

ARCHITECTURE ecu OF p6 IS
BEGIN
	
	WITH X SELECT
		D7 <= "1000000" WHEN "0000",
		      "1111001" WHEN "0001",
		      "0100100" WHEN "0010",
		      "0110000" WHEN "0011",
		      "0011001" WHEN "0100",
		      "0010010" WHEN "0101",
		      "0000010" WHEN "0110",
		      "1111000" WHEN "0111",
		      "0000000" WHEN "1000",
		      "0010000" WHEN "1001",
		      "1111111" WHEN OTHERS;
				 
END ecu;
