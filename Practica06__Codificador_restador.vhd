LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;
USE IEEE.STD_LOGIC_UNSIGNED.ALL;
USE IEEE.NUMERIC_STD.ALL;

ENTITY p6 IS
PORT (
	N1, N2: IN STD_LOGIC_VECTOR(2 DOWNTO 0);
	D7: OUT STD_LOGIC_VECTOR(8 DOWNTO 0);
	NEG : OUT STD_LOGIC
);
END p6;

ARCHITECTURE ecu OF p6 IS

	SIGNAL RES, COMP : STD_LOGIC_VECTOR(3 DOWNTO 0);

BEGIN

	RES <= ('0' & N1) - ('0' & N2);
	
	NEG <= NOT RES(3);
	
	PROCESS(RES(3))
		VARIABLE TEMP : STD_LOGIC_VECTOR(3 DOWNTO 0);
	BEGIN
		IF RES(3) = '1' THEN
			TEMP := STD_LOGIC_VECTOR(unsigned(NOT RES) + 1);
			COMP <= TEMP;
		ELSE
			COMP <= RES;
		END IF;
	END PROCESS;
	
	
	WITH COMP SELECT
		D7 <= "111000000" WHEN "0000",
		      "111111001" WHEN "0001",
		      "110100100" WHEN "0010",
		      "110110000" WHEN "0011",
		      "110011001" WHEN "0100",
		      "110010010" WHEN "0101",
		      "110000010" WHEN "0110",
		      "111111000" WHEN "0111",
		      "111111111" WHEN OTHERS;
				 
END ecu;
