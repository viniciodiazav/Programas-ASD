LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;
USE IEEE.NUMERIC_STD.ALL;
USE IEEE.STD_LOGIC_UNSIGNED.ALL;

ENTITY p7 IS
PORT (
	CLKI : IN STD_LOGIC;
	D16: OUT STD_LOGIC_VECTOR(15 DOWNTO 0)
);
END p7;

ARCHITECTURE ecu OF p7 IS

	SIGNAL COUNT : STD_LOGIC_VECTOR(27 DOWNTO 0);

	SIGNAL NUM : STD_LOGIC_VECTOR(2 DOWNTO 0);
	
BEGIN

	PROCESS(CLKI)
	BEGIN
		
		IF rising_edge(CLKI) THEN
			IF COUNT = "0001011111010111100001000000" THEN
				COUNT <= (OTHERS => '0');
				IF NUM = "111" THEN
					NUM <= "000";
				ELSE
					NUM <= NUM + '1';
				END IF;
			ELSE
				COUNT <= COUNT + '1';
			END IF;
		END IF;
	END PROCESS;

	WITH NUM SELECT
        D16 <= "1011001111111011" WHEN "000",
               "0111110001110110" WHEN "001",
               "1001011110011101" WHEN "010",
               "0111110001110110" WHEN "011",
               "0011010011111110" WHEN "100",
               "0111110001110110" WHEN "101",
               "0011010011011100" WHEN "110",
               "1111111111111111" WHEN OTHERS;

	

END ecu;
